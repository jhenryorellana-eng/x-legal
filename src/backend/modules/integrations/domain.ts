// Lenient UUID — accepts both proper UUID and arbitrary external IDs
const zUuid = z.string().min(1);

/** Single document in the POST payload (DOC-70 §2.4). */
export const AbogadosDocumentSchema = z.object({
  name: z.string().min(1),
  kind: z.enum(DOCUMENT_KINDS).default("declaration"),
  content: z.string().min(1),
});

export type AbogadosDocument = z.infer<typeof AbogadosDocumentSchema>;

/** Review block (V2.0 always sends null — DOC-70 §2.5). */
export const AbogadosReviewSchema = z
  .object({
    score: z.number().int().min(0).max(100),
    ready_to_file: z.boolean(),
    summary: z.string(),
    findings: z.array(z.unknown()),
    strengths: z.array(z.string()),
    playbook_name: z.string().nullish(),
    reviewer_model: z.string().nullish(),
  })
  .nullish();

/** POST /api/integration/validations payload (DOC-70 §2.1). */
