export interface AnnexIndexItem {
  position: number;
  title: string;
  item_type: string;
  page_count: number | null;
  /** True if text travels in the package (ai_generation, automated_form, client_document with extraction). */
  textIncluded: boolean;
}

/**
 * Builds the "Índice del expediente y anexos no incluidos" document.
 *
 * Always the LAST document in the SaaS package. Lists ALL expediente items
 * in position order so the lawyer knows what physical evidence exists.
 *
 * Returns an AbogadosDocument ready to append to the documents[] array.
 */
export function buildAnnexIndex(
  items: AnnexIndexItem[],
): { name: string; kind: "other"; content: string } {
  const sorted = [...items].sort((a, b) => a.position - b.position);

  const lines: string[] = ["Índice del expediente", ""];

  for (const item of sorted) {
    const pageInfo = item.page_count != null ? ` | ${item.page_count} pág.` : "";
    const textInfo = item.textIncluded
      ? "[texto incluido en paquete]"
      : "[solo referencia — no incluido]";
    lines.push(
      `${item.position}. ${item.title} (${item.item_type}${pageInfo}) — ${textInfo}`,
    );
  }

  return {
    name: "Índice del expediente y anexos no incluidos",
    kind: "other",
    content: lines.join("\n"),
  };
}
