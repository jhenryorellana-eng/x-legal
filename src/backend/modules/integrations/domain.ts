/**
 * Derives the canonical "{F}. {LastName}" label from a client's name.
 *
 * Delegates to `canonicalClientLabel` from the expediente module (same rule).
 * DOC-70 §2.3: initial of first name + first word of last name (with particle).
 * The expediente module's implementation takes the full last name as-is.
 *
 * Example: "Juan", "Vásquez" → "J. Vásquez"
 * Example: "María José", "De La Cruz" → "M. De La Cruz"
 */
export function buildClientLabel(firstName: string, lastName: string): string {
  return canonicalClientLabel(firstName, lastName);
}

// ---------------------------------------------------------------------------
// SerializeAutomatedForm input type
// ---------------------------------------------------------------------------

export interface SerializeFormQuestion {
  id: string;
  question_i18n: { es: string; en?: string };
  pdf_field_name: string;
  field_type: string;
  is_required: boolean;
  options: Array<{ value: string; label_i18n: { es: string; en?: string } }> | null;
  answer: unknown;
}

export interface SerializeFormGroup {
  id: string;
  title_i18n: { es: string; en?: string };
  position: number;
  questions: SerializeFormQuestion[];
}

export interface SerializeFormInput {
  /** Human-readable form label (e.g. "I-360 — Petition for Amerasian"). */
  label: string;
  /** Numeric version of the published automation. */
  versionNo: number;
  /** Label for the version (e.g. "publicada"). */
  versionLabel: string;
  /** Optional party label (e.g. "Mateo V. (minor)"). */
  partyLabel: string | null;
  groups: SerializeFormGroup[];
}

// ---------------------------------------------------------------------------
// serializeAutomatedForm — DOC-70 §2.4.1
// ---------------------------------------------------------------------------

/**
 * Serializes an automated form (AcroForm responses) into deterministic
 * plain text for the SaaS Abogados validation package.
 *
 * Format (DOC-70 §2.4.1):
 *   FORMULARIO: {label}
 *   Versión de automatización: {n} ({versionLabel})  ·  Parte: {partyLabel?}
 *
 *   ### {group.title_i18n.es}
 *   {question_i18n.es} [{pdf_field_name}]: {value}
 *   ...
 *
 * Value formatting rules:
 *   - date: YYYY-MM-DD (passthrough — stored as ISO date string)
 *   - checkbox: "Sí" / "No"
 *   - select: "{label_i18n.es} ({value})"
 *   - required + null answer: "[SIN RESPUESTA]"
 *   - optional + null answer: skipped (empty line not emitted)
 */
export function serializeAutomatedForm(form: SerializeFormInput): string {
  const lines: string[] = [];

  // Header
  lines.push(`FORMULARIO: ${form.label}`);
  const versionLine = `Versión de automatización: ${form.versionNo} (${form.versionLabel})`;
  if (form.partyLabel) {
    lines.push(`${versionLine}  ·  Parte: ${form.partyLabel}`);
  } else {
    lines.push(versionLine);
  }

  // Groups (sorted by position ascending)
  const sortedGroups = [...form.groups].sort((a, b) => a.position - b.position);

  for (const group of sortedGroups) {
    lines.push("");
    lines.push(`### ${group.title_i18n.es}`);

    for (const q of group.questions) {
      const formattedValue = formatQuestionValue(q);
      if (formattedValue === null) {
        // Optional with no answer — skip
        continue;
      }
      lines.push(`${q.question_i18n.es} [${q.pdf_field_name}]: ${formattedValue}`);
    }
  }

  return lines.join("\n");
}

function formatQuestionValue(q: SerializeFormQuestion): string | null {
  const { answer, field_type, is_required, options } = q;

  if (answer === null || answer === undefined || answer === "") {
    if (is_required) return "[SIN RESPUESTA]";
    return null; // optional + no answer → skip
  }

  if (field_type === "checkbox") {
    return answer ? "Sí" : "No";
  }

  if (field_type === "select" && options) {
    const found = options.find((o) => o.value === answer);
    if (found) {
      return `${found.label_i18n.es} (${found.value})`;
    }
    return String(answer);
  }

  // text, date, number, textarea — passthrough
  return String(answer);
}

