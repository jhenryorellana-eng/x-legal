// CI gate — public/ asset budget (RNF-035 / DOC-19). Fails if any committed
// static asset exceeds 1 MB, so a fat image/font can never ship to the client.
// The service worker (public/sw.js) is a generated build artifact and is skipped.
import { readdirSync, statSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = "public";
const LIMIT = 1_000_000; // 1 MB (decimal) — conservative.
const SKIP = new Set(["sw.js", "sw.js.map"]); // generated by Serwist.

/** @returns {string[]} */
function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(p));
    else if (!SKIP.has(entry.name) && !entry.name.startsWith("swe-worker-")) out.push(p);
  }
  return out;
}

const offenders = walk(ROOT)
  .map((p) => ({ p, size: statSync(p).size }))
  .filter(({ size }) => size > LIMIT)
  .sort((a, b) => b.size - a.size);

if (offenders.length) {
  console.error(`check-asset-size: ${offenders.length} asset(s) over ${(LIMIT / 1000).toFixed(0)} KB:`);
  for (const { p, size } of offenders) {
    console.error(`  - ${relative(ROOT, p)} — ${(size / 1024).toFixed(0)} KB`);
  }
  process.exit(1);
}
console.log(`check-asset-size: OK (no public/ asset over ${(LIMIT / 1000).toFixed(0)} KB)`);
